// ===== UTILITY FUNCTIONS =====

// Get category by ID
function getCategoryById(id) {
    return categories.find(category => category.id === id);
}

// Get author by ID
function getAuthorById(id) {
    return authors.find(author => author.id === id);
}

// Get posts by category
function getPostsByCategory(categoryId) {
    return blogPosts.filter(post => 
        post.category.toLowerCase().replace(/\s+/g, '-') === categoryId
    );
}

// Get featured posts
function getFeaturedPosts() {
    return blogPosts.filter(post => post.featured);
}

// Get recent posts
function getRecentPosts(limit = 5) {
    return blogPosts
        .sort((a, b) => new Date(b.date) - new Date(a.date))
        .slice(0, limit);
}

// Search posts
function searchPosts(query) {
    const searchTerm = query.toLowerCase();
    return blogPosts.filter(post => {
        return post.title.toLowerCase().includes(searchTerm) ||
               post.excerpt.toLowerCase().includes(searchTerm) ||
               post.content.toLowerCase().includes(searchTerm) ||
               post.author.toLowerCase().includes(searchTerm) ||
               post.category.toLowerCase().includes(searchTerm) ||
               post.tags.some(tag => tag.toLowerCase().includes(searchTerm));
    });
}

// Get related posts
function getRelatedPosts(currentPostId, limit = 3) {
    const currentPost = blogPosts.find(post => post.id === currentPostId);
    if (!currentPost) return [];
    
    // Find posts in the same category
    let relatedPosts = blogPosts.filter(post => 
        post.id !== currentPostId && 
        post.category === currentPost.category
    );
    
    // If not enough, add posts with similar tags
    if (relatedPosts.length < limit) {
        const tagRelatedPosts = blogPosts.filter(post => 
            post.id !== currentPostId && 
            post.category !== currentPost.category &&
            post.tags.some(tag => currentPost.tags.includes(tag))
        );
        relatedPosts = [...relatedPosts, ...tagRelatedPosts];
    }
    
    // If still not enough, add recent posts
    if (relatedPosts.length < limit) {
        const recentPosts = blogPosts
            .filter(post => post.id !== currentPostId && !relatedPosts.includes(post))
            .sort((a, b) => new Date(b.date) - new Date(a.date));
        relatedPosts = [...relatedPosts, ...recentPosts];
    }
    
    return relatedPosts.slice(0, limit);
}

// Format date
function formatDate(dateString) {
    const options = { year: 'numeric', month: 'long', day: 'numeric' };
    return new Date(dateString).toLocaleDateString('en-US', options);
}

// Calculate reading time
function calculateReadingTime(content) {
    const wordsPerMinute = 200;
    const words = content.split(/\s+/).length;
    const minutes = Math.ceil(words / wordsPerMinute);
    return `${minutes} min read`;
}

// Generate excerpt from content
function generateExcerpt(content, length = 150) {
    const text = content.replace(/<[^>]*>/g, ''); // Remove HTML tags
    return text.length > length ? text.substring(0, length) + '...' : text;
}

